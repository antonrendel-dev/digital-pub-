export function getVacancyContextBlock(tagSlugs: string[]): ContextBlock | null {
  const slugSet = new Set(tagSlugs)

  const specSlug = [
    'smm',
    'marketing',
    'dizajn',
    'copywriting',
    'target',
    'seo',
    'analitika',
    'razrabotka',
  ].find((s) => slugSet.has(s))

  if (!specSlug) return null

  const base = SPECIALIZATION_BLOCKS[specSlug]
  const tips = [...base.tips]

  const levelSlug = ['junior', 'middle', 'senior'].find((s) => slugSet.has(s))
  if (levelSlug) tips.push(LEVEL_ADDONS[levelSlug])

  const formatSlug = ['udalyonka', 'gibrid', 'ofis'].find((s) => slugSet.has(s))
  if (formatSlug) tips.push(FORMAT_ADDONS[formatSlug])

  return { heading: base.heading, tips, cta: base.cta }
}
