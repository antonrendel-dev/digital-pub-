function parseSalaryNumber(salary: string): number | null {
  const cleaned = salary.replace(/\s/g, '')
  const nums = cleaned.match(/\d{4,7}/g)
  if (!nums) return null
  const values = nums.map(Number).filter((n) => n >= 10000 && n <= 1000000)
  if (!values.length) return null
  return Math.round(values.reduce((a, b) => a + b, 0) / values.length)
}

function getDaysAgo(createdAt: string | Date): number {
  const date = typeof createdAt === 'string' ? new Date(createdAt) : createdAt
  return Math.floor((Date.now() - date.getTime()) / 86400000)
}

function buildIntro(input: VacancyContextInput, specSlug: string): string {
  const specNames: Record<string, string> = {
    smm: 'SMM',
    marketing: 'маркетинга',
    dizajn: 'дизайна',
    copywriting: 'копирайтинга',
    target: 'таргетированной рекламы',
    seo: 'SEO',
    analitika: 'аналитики',
    razrabotka: 'разработки',
  }
  const specName = specNames[specSlug] ?? 'этой специализации'

  const parts: string[] = []

  if (input.company) {
    parts.push(`Компания ${input.company} ищет специалиста в области ${specName}.`)
  }

  if (input.salary) {
    const num = parseSalaryNumber(input.salary)
    if (num) {
      const formatted = num.toLocaleString('ru-RU')
      parts.push(`Предлагаемая зарплата — ${formatted} ₽.`)
    }
  }

  if (input.createdAt) {
    const days = getDaysAgo(input.createdAt)
    if (days === 0) parts.push('Вакансия опубликована сегодня — отклик сейчас даёт преимущество.')
    else if (days === 1) parts.push('Вакансия опубликована вчера — откликайтесь быстро.')
    else if (days <= 3) parts.push(`Свежая вакансия — ${days} дня назад.`)
  }

  return parts.join(' ')
}

export function getVacancyContextBlock(input: VacancyContextInput): ContextBlock | null {
  const slugSet = new Set(input.tagSlugs)

  const specSlug = [
    'smm',
    'marketing',
    'dizajn',
    'copywriting',
    'target',
    'seo',
    'analitika',
    'razrabotka',
  ].find((s) => slugSet.has(s))

  if (!specSlug) return null

  const tips = [...(SPECIALIZATION_TIPS[specSlug] ?? [])]

  const levelSlug = ['junior', 'middle', 'senior'].find((s) => slugSet.has(s))
  if (levelSlug) tips.push(LEVEL_TIPS[levelSlug])

  const formatSlug = ['udalyonka', 'gibrid', 'ofis'].find((s) => slugSet.has(s))
  if (formatSlug) tips.push(FORMAT_TIPS[formatSlug])

  const intro = buildIntro(input, specSlug)

  return {
    heading: 'Как получить эту вакансию',
    tips: intro ? [intro, ...tips] : tips,
  }
}
