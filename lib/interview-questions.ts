const specializationSlugs = Object.keys(interviewData)

function detectSpecialization(tagSlugs: string[]): string | null {
  for (const slug of tagSlugs) {
    if (specializationSlugs.includes(slug)) {
      return slug
    }
  }
  return null
}

export function getInterviewQuestions(tagSlugs: string[]): InterviewQuestion[] {
  const specialization = detectSpecialization(tagSlugs)
  if (!specialization) return []
  return interviewData[specialization] ?? []
}
