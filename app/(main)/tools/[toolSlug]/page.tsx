export function generateStaticParams() {
  return Object.keys(TOOLS).map((slug) => ({ toolSlug: slug }))
}

interface Props {
  params: Promise<{ toolSlug: string }>
}

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { toolSlug } = await params
  const tool = TOOLS[toolSlug]
  if (!tool) return { title: 'Инструмент не найден' }

  const canonical = `${BASE_URL}/tools/${tool.slug}`
  return {
    title: tool.metaTitle,
    description: tool.metaDescription,
    alternates: { canonical },
    openGraph: {
      title: tool.metaTitle,
      description: tool.metaDescription,
      url: canonical,
      type: 'website',
    },
  }
}

export default async function ToolPage({ params }: Props) {
  const { toolSlug } = await params
  const tool = TOOLS[toolSlug]
  if (!tool) notFound()

  const { posts, total } = await getPostsByTool(tool.query)

  const breadcrumbLd = {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    itemListElement: [
      { '@type': 'ListItem', position: 1, name: 'Главная', item: BASE_URL },
      { '@type': 'ListItem', position: 2, name: 'Инструменты', item: `${BASE_URL}/tools` },
      { '@type': 'ListItem', position: 3, name: tool.name, item: `${BASE_URL}/tools/${tool.slug}` },
    ],
  }

  const faqItems = TOOL_FAQS[toolSlug] ?? []
  const faqLd =
    faqItems.length > 0
      ? {
          '@context': 'https://schema.org',
          '@type': 'FAQPage',
          mainEntity: faqItems.map(({ q, a }) => ({
            '@type': 'Question',
            name: q,
            acceptedAnswer: { '@type': 'Answer', text: a },
          })),
        }
      : null

  return (
    <PageShell>
      <JsonLd data={breadcrumbLd} />
      {faqLd && <JsonLd data={faqLd} />}
      <div className="max-w-wrap mx-auto px-4 pt-6 pb-12">
        <nav className="flex items-center gap-2 text-sm text-text-muted mb-6">
          <Link href="/" className="no-underline hover:text-text transition-colors">
            Главная
          </Link>
          <span>&#8250;</span>
          <Link href="/tools" className="no-underline hover:text-text transition-colors">
            Инструменты
          </Link>
          <span>&#8250;</span>
          <span className="text-text">{tool.name}</span>
        </nav>

        <h1 className="text-2xl md:text-3xl font-bold text-text mb-2">{tool.h1}</h1>
        <p className="text-text-muted mb-6">
          Актуальные вакансии, где требуется {tool.name} — собраны из Telegram-каналов digital-рынка
        </p>

        <div className="flex items-center gap-3 mb-6">
          <div className="flex items-center gap-1.5 bg-bg-card border border-border rounded-lg px-3 py-2 text-sm">
            <span className="text-text-muted">Найдено вакансий:</span>
            <strong className="text-text">{total}</strong>
          </div>
        </div>

        {posts.length === 0 ? (
          <div className="py-9 text-center text-text-light text-sm border border-dashed border-border rounded-lg">
            Пока нет вакансий с упоминанием {tool.name}
          </div>
        ) : (
          <VacancyGrid posts={posts} />
        )}

        <article
          className="mt-12 pt-8 border-t border-border prose prose-sm max-w-none"
          dangerouslySetInnerHTML={{ __html: tool.seoText }}
        />

        {faqItems.length > 0 && (
          <section className="mt-8 pt-6 border-t border-border">
            <h2 className="text-lg font-semibold text-text mb-4">Частые вопросы</h2>
            <div className="space-y-3">
              {faqItems.map(({ q, a }, i) => (
                <details key={i} className="group border border-border rounded-lg">
                  <summary className="flex justify-between items-center cursor-pointer px-4 py-3 text-sm font-medium text-text list-none">
                    {q}
                    <span className="text-text-muted group-open:rotate-180 transition-transform">
                      &#9662;
                    </span>
                  </summary>
                  <div className="px-4 pb-4 pt-1 text-sm text-text-muted">{a}</div>
                </details>
              ))}
            </div>
          </section>
        )}
      </div>
    </PageShell>
  )
}
